// math operation short hand.

// sum short hand.
var price1 = 20;
var price2 = 30;
var sum = price1 + price2;
console.log('Total Price =', sum);

// I want to increase price1. there is two way to increase. they are given bellow:

// 1
var increasePrice1 = price1 + 20;
console.log('New Price of Price1 =',increasePrice1);

// 2
// shorthand way 1.
// price1 = price1 + 20;

// shorthand way 2.
price1 += 20;
console.log('Without Change Price1 variable and add New Price =', price1);

// if we want to increase only one or add/sum only one then we can do the bellow code:
var age = 29;
age++;
console.log('Age increment only One =', age);


// In this way we can multiplication, division, add/sum, subtraction/deduction of any amount and it is the shorthand way.
