// normal product price declare. 
var onionPrice = 45;
var eggPrice = 25;
var eggQuantity = 6;
console.log(onionPrice);
console.log(eggPrice);

// sum of those products.
var totalPrice = onionPrice + eggPrice;
console.log("Total Price =", totalPrice);
console.log(onionPrice + eggPrice); // we can call it directly. it will show total price also. 

// subtraction/deduction of those products.
var priceDifference = onionPrice - eggPrice;
console.log("Price Difference =", priceDifference);
console.log(onionPrice - eggPrice); // we can call it directly. it will show total price also.

// multiplication of those products.
var totalEggPrice = eggPrice * eggQuantity;
console.log("Total Egg Price =", totalEggPrice);
console.log(eggPrice * eggQuantity); // we can call it directly. it will show total price also.

// division those products.
var perPiecePriceEgg = totalEggPrice / eggQuantity;
console.log('Per piece price =', perPiecePriceEgg);
console.log(totalEggPrice / eggQuantity); // we can call it directly. it will show total price also.