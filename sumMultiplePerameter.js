// Summation:
function sum(num1, num2){
    let sum = num1 + num2;
    return sum;
}

let totalSum = sum(50, 80);
console.log('Total Sum:', totalSum);

// Multiplication:
function multi(num1, num2){
    let multi = num1 * num2;
    return multi;
}

let totalMulti = multi(50, 80);
console.log('Total Multiplication:', totalMulti);